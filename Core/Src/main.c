/*!
  * \brief Reads x, y and z accelerometer axis from the mpu6050,
  *  formats the data into a string and prints the data over the uart.
  */
void print_mpu6050_axis_data(void)
{
	// get all 3 latest axis values
	float x_value = mpu6050_get_x_axis_data();
	float y_value = mpu6050_get_y_axis_data();
	float z_value = mpu6050_get_z_axis_data();

	char x_axis_msg[MAX_BUFFER_LEN] = "";
	char y_axis_msg[MAX_BUFFER_LEN] = "";
	char z_axis_msg[MAX_BUFFER_LEN] = "";

	snprintf(x_axis_msg, MAX_BUFFER_LEN, "x axis: %.1f  ", x_value);
	snprintf(y_axis_msg, MAX_BUFFER_LEN, "y axis: %.1f  ", y_value);
	snprintf(z_axis_msg, MAX_BUFFER_LEN, "z axis: %.1f\r\n", z_value);

	uint16_t x_axis_buffer_len = strlen(x_axis_msg);
	periph_uart_send_tx(x_axis_msg, x_axis_buffer_len);
	uint16_t y_axis_buffer_len = strlen(y_axis_msg);
	periph_uart_send_tx(y_axis_msg, y_axis_buffer_len);
	uint16_t z_axis_buffer_len = strlen(z_axis_msg);
	periph_uart_send_tx(z_axis_msg, z_axis_buffer_len);
}

void print_temperature_data(void)
{
	float temp_value = mpu6050_get_temperature_data();
	// comes in as C, convert to F
	// (C Ã— 9/5) + 32
	float temp_value_F = (temp_value * 1.8) + 32.0;
	char temp_msg[MAX_BUFFER_LEN] = "";

	snprintf(temp_msg, MAX_BUFFER_LEN, "temperature is: %.1f\r\n", temp_value_F);

	// output over the uart to serial port
	uint16_t temp_buffer_len = strlen(temp_msg);
	periph_uart_send_tx(temp_msg, temp_buffer_len);

	// output to the OLED display
	ssd1306_Fill(Black);
	ssd1306_SetCursor(2, 0);
	char temp_F[MAX_BUFFER_LEN] = "";
	snprintf(temp_F, MAX_BUFFER_LEN, "Temp: %.1fF", temp_value_F);
	ssd1306_WriteString(temp_F, Font_11x18, White);
}

